package shopnow.domain;

public class ClientFile {
	private int clientFile_id;
	private String clientName;
	private String clientContact;
	private String clientAddress;
	private String clientFilePath;
	public int getClientFile_id() {
		return clientFile_id;
	}
	public void setClientFile_id(int clientFile_id) {
		this.clientFile_id = clientFile_id;
	}
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getClientContact() {
		return clientContact;
	}
	public void setClientContact(String clientContact) {
		this.clientContact = clientContact;
	}
	public String getClientAddress() {
		return clientAddress;
	}
	public void setClientAddress(String clientAddress) {
		this.clientAddress = clientAddress;
	}
	public String getClientFilePath() {
		return clientFilePath;
	}
	public void setClientFilePath(String clientFilePath) {
		this.clientFilePath = clientFilePath;
	}
	
	
}
